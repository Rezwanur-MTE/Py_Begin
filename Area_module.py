def Triangle(b,h):
    print(f"The area of triangle is {0.5*b*h}")

def Rectangle(a,b):
    print(f"The area of rectangle is {a*b}")

def Trapizium(a,b,h):
    print(f"The area of trapizium is {0.5*(a+b)*h}")

